export function changePageContent (pageID){
    let pageName = pageID + "Content";
    $("#app").html(eval(pageName));
    console.log(pageID);

    if (pageID == "home") {
      $("nav a").css('color', "#fff")
      $("nav #logo span").css('color', '#fff')
      $("nav #logo").css('border-color', '#fff')
      $("nav #bottom").css('border-color', '#fff')

  } else{
      $("nav *").css('color', "black")
      $("nav #links span").css('color', '#EE4367')
      $("nav #logo").css('border-color', 'black')
      $("nav #bottom").css('border-color', 'black')
  }
}

